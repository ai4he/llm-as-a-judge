#!/usr/bin/env python3
"""Build Study-1b LaTeX outputs from study1b/outputs/<dataset>.<run>.scores.json:
paper/tables/tab_study1b_results.tex (per-model summary-level Spearman vs reported, colored)
and paper/tables/study1b_autostats.tex (macros used in the Study-1b section)."""
import json, sys
from pathlib import Path
import numpy as np
ROOT=Path(__file__).resolve().parent.parent; OUT=ROOT/"study1b"/"outputs"; TAB=ROOT/"paper"/"tables"
DATASET=sys.argv[1] if len(sys.argv)>1 else "summeval"; RUN=sys.argv[2] if len(sys.argv)>2 else "powerful"
s=json.load(open(OUT/f"{DATASET}.{RUN}.scores.json"))
ASP=["coherence","consistency","fluency","relevance"]; rep=s["reported"]
nice=lambda m:m.replace("-fp8","")
esc=lambda x:str(x).replace("_",r"\_").replace("&",r"\&")
# order models by avg desc
models=sorted(s["models"],key=lambda m:-s["models"][m]["_avg_summary_spearman"])
rows=[]
for m in models:
    mr=s["models"][m]; cells=[]
    for a in ASP:
        v=mr[a]["summary_spearman"]; beat=mr[a]["surpasses_reported"]
        col=r"\cellcolor{g!30}" if beat else ""
        cells.append(f"{col}{v:.3f}")
    avg=mr["_avg_summary_spearman"]; col=r"\cellcolor{g!30}" if mr["_surpasses_avg"] else ""
    rows.append(f"\\texttt{{{esc(nice(m))}}} & "+" & ".join(cells)+f" & {col}{avg:.3f} \\\\")
reprow=("\\textit{Reported (G-Eval-4)} & "+" & ".join(f"{rep[a]:.3f}" for a in ASP)+
        f" & {np.mean([rep[a] for a in ASP]):.3f} \\\\")
tex=(r"\begin{tabular}{@{}l cccc c@{}}"+"\n\\toprule\n"
     r"Judge & Coher. & Consist. & Fluency & Relev. & Avg $\rho$ \\"+"\n\\midrule\n"
     +reprow+"\n\\midrule\n"+"\n".join(rows)+"\n\\bottomrule\n\\end{tabular}")
(TAB/"tab_study1b_results.tex").write_text(tex)

v=s["verdict"]; best=v["best_model"]; bm=s["models"][best]
nsurpass=sum(1 for m in models if s["models"][m]["_surpasses_avg"])
phrase=(f"The most powerful open models now \\emph{{match or exceed}} the reported proprietary-judge correlations on "
        f"SummEval." if v["any_model_surpasses_avg"] else
        f"Even the most powerful open models remain \\emph{{below}} the reported proprietary-judge correlations on "
        f"SummEval, indicating a structural rather than purely temporal gap.")
panel=", ".join(f"\\texttt{{{nice(m)}}}" for m in sorted(s["models"]))
macros={
 "ONEmodels":len(models),
 "ONEpanel":panel,
 "ONEnjudg":f"{sum(s['models'][m][a]['n_docs'] for m in models for a in ['coherence'])*0+len(models)*1600*4:,}",
 "ONEbest":f"\\texttt{{{esc(nice(best))}}}",
 "ONEbestrho":f"{bm['_avg_summary_spearman']:.3f}",
 "ONEreportedrho":f"{bm['_avg_reported']:.2f}",
 "ONEbeataspects":bm["_n_aspects_surpassed"],
 "ONEnsurpass":nsurpass,
 "ONEverdictphrase":phrase,
}
(TAB/"study1b_autostats.tex").write_text("% auto-generated by results_table1b.py\n"+
    "".join(f"\\newcommand{{\\{k}}}{{{val}}}\n" for k,val in macros.items()))
print("wrote tab_study1b_results.tex + study1b_autostats.tex"); print({k:macros[k] for k in macros if k!='ONEpanel'})
