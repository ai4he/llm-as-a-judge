#!/usr/bin/env python3
"""
build_corpus.py
-----------------------------------------------------------------------------
Constructs the extracted-studies dataset for the PRISMA systematic review
  "LLM-as-a-Judge versus Human Annotation: Domain- and Task-Level Reliability"

Outputs (all written next to this file, under data/):
  - included_studies.csv / .json   the coded corpus (one row per included study)
  - search_log.csv                 the 59 executed search query strings + blocks
  - prisma_counts.json             the PRISMA 2020 flow counts (internally consistent)

Each record is grounded in a real paper retrieved during the search (2026-05-28).
Numeric agreement values are transcribed from the source where reported; where a
study reports only a qualitative comparison, `agreement` is left null and the
ordinal `verdict` (see protocol §8) carries the synthesis.

Metric codes (scale): kappa, fleiss_kappa, krippendorff_alpha, icc, pearson,
spearman, kendall, qwk, gwet_ac2, pct_agreement (0-1), accuracy (0-1),
f1 (0-1), winrate_corr (0-1). Coefficient metrics share a roughly [−1,1] /
[0,1] scale; `metric_family` groups them for honest within-family plotting.
"""
