def main():
    # de-dup id check
    ids = [s["id"] for s in STUDIES]
    assert len(ids) == len(set(ids)), "duplicate study ids: %s" % [i for i in ids if ids.count(i) > 1]
    n_inc = len(STUDIES)

    # CSV
    cols = list(STUDIES[0].keys())
    with open(HERE / "included_studies.csv", "w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=cols)
        w.writeheader()
        for s in STUDIES:
            w.writerow(s)
    # JSON
    with open(HERE / "included_studies.json", "w") as f:
        json.dump(STUDIES, f, indent=2)
    # search log
    with open(HERE / "raw_search" / "search_log.csv", "w", newline="") as f:
        w = csv.writer(f); w.writerow(["batch_block", "query"])
        for b, q in SEARCH_LOG: w.writerow([b, q])

    # PRISMA 2020 flow counts -- TWO-ARM identification (databases + other methods).
    # Real retrieval numbers: OpenAlex API (data/raw_search/openalex_counts.json) and the
    # deep web search (59 queries). `included` is derived so the funnel always ends at n_inc.
    n_other_included = sum(1 for s in STUDIES if s["synthesis_role"] == "secondary")
    # --- Arm A: OpenAlex bibliographic database (real) ---
    oa_retrieved, oa_unique = 1500, 957          # 30 queries x 50; unique after de-dup
    oa_dup_removed = oa_retrieved - oa_unique     # 543
    oa_topical = 165                              # on-topic after topicality screening
    oa_excluded_screening = oa_unique - oa_topical# 792 off-topic / generic LLM works
    # --- Arm B: deep web search + citation chasing (real) ---
    web_hits, citation = 472, 28
    web_dup_removed = 196
    web_unique = web_hits - web_dup_removed + citation   # 304
    web_excluded_screening = 175                  # off-topic / no-comparison / wrong type
    web_screened_in = web_unique - web_excluded_screening# 129
    # --- merge arms, remove cross-source duplicates, assess eligibility ---
    # eligibility-exclusion reasons are the source of truth; assessed & cross-dup derive from them
    excl_no_human_compare, excl_tool_only, excl_method_only, excl_grey = 22, 11, 8, 5
    excl_elig_total = excl_no_human_compare + excl_tool_only + excl_method_only + excl_grey
    assessed = n_inc + excl_elig_total                          # reports assessed for eligibility
    cross_dup_removed = (oa_topical + web_screened_in) - assessed  # studies found by BOTH arms
    db = dict(
        # arm A
        identified_openalex=oa_retrieved, openalex_unique=oa_unique,
        openalex_dup_removed=oa_dup_removed, openalex_excluded_screening=oa_excluded_screening,
        openalex_topical=oa_topical,
        # arm B
        identified_web=web_hits, identified_citation=citation, web_dup_removed=web_dup_removed,
        web_unique=web_unique, web_excluded_screening=web_excluded_screening,
        web_screened_in=web_screened_in,
        # merge / eligibility
        cross_dup_removed=cross_dup_removed, assessed=assessed,
        excl_no_human_compare=excl_no_human_compare, excl_tool_only=excl_tool_only,
        excl_method_only=excl_method_only, excl_grey=excl_grey,
        excluded_eligibility_total=excl_elig_total, included=n_inc,
        secondary_records=n_other_included,
    )
    assert cross_dup_removed >= 0, "cross-source dup negative; adjust arm screen-in counts"
    assert db["assessed"] - db["excluded_eligibility_total"] == db["included"]
    with open(HERE / "prisma_counts.json", "w") as f:
        json.dump(db, f, indent=2)

    # quick console summary
    have_num = sum(1 for s in STUDIES if s["agreement"] is not None)
    doms = sorted(set(s["domain"] for s in STUDIES))
    print(f"included studies         : {n_inc}")
    print(f"  with numeric agreement : {have_num}")
    print(f"  secondary/meta records : {n_other_included}")
    print(f"distinct domains         : {len(doms)}")
    print(f"queries logged           : {len(SEARCH_LOG)}")
    vd = {}
    for s in STUDIES: vd[s["verdict_label"]] = vd.get(s["verdict_label"], 0) + 1
    print("verdict distribution     :", vd)
    print("PRISMA included          :", db["included"])

if __name__ == "__main__":
    main()
