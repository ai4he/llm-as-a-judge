def S(id, authors, year, venue, arxiv, domain, task, subjectivity, expertise,
      judge, comparator, metric, agreement, human_baseline, parity, verdict,
      biases, n, role, finding):
    if authors == "authors": authors = AUTHORS_OVERRIDE.get(id, authors)
    return dict(id=id, authors=authors, year=year, venue=venue, arxiv=arxiv,
                domain=domain, task=task, subjectivity=subjectivity,
                expertise=expertise, judge_models=judge, comparator=comparator,
                metric=metric, metric_family=METRIC_FAMILY.get(metric, "n/a"),
                agreement=agreement, human_baseline=human_baseline, parity=parity,
                verdict=verdict, verdict_label=VERDICT_LABEL[verdict],
                reliability_score=6 - verdict, biases=biases, n=n,
                synthesis_role=role, finding=finding)

